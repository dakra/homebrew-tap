  # Installed from prebuilt wheels rather than source (see the resource comment).
  PYOBJC = %w[pyobjc-core pyobjc-framework-Cocoa pyobjc-framework-Quartz].freeze

  def install
    venv = virtualenv_create(libexec, "python3.13")

    # Everything except pyobjc builds cleanly from source.
    venv.pip_install(resources.reject { |r| PYOBJC.include?(r.name) })

    # Install the pyobjc wheels with a plain pip call so Homebrew's global
    # `--no-binary=:all:` does not force a (failing) source build. Stage each
    # resource so pip sees a cleanly named wheel (the cache path is prefixed
    # with a checksum that pip cannot parse).
    python = Formula["python@3.13"].opt_bin/"python3.13"
    PYOBJC.each do |name|
      resource(name).stage do
        system python, "-m", "pip", "--python=#{libexec}/bin/python", "install",
               "--no-deps", "--ignore-installed", "--no-compile", Dir["*.whl"].fetch(0)
      end
    end

    venv.pip_install_and_link buildpath
  end

  def caveats
    <<~EOS
      elate drives a real Emacs; install one if you have not already, e.g.:
        brew install --cask emacs        # GUI build
        brew install emacs-plus          # via d12frosted/emacs-plus
    EOS
  end

